package app.modelos.prescritivos;

public class Cep {

	// Attributes
	private String numero;
	private String nome;
	
	
	//constructor
	public Cep(String numero, String nome) {
		this.numero = numero;
		this.nome = nome;
	}
	
	//methods
	public String pesquisarLogradouro() {
		return nome;
	}
	
}
